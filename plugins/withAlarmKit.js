const { IOSConfig, withInfoPlist, withXcodeProject } = require('@expo/config-plugins');
const fs = require('fs');
const path = require('path');

const ALARM_KIT_USAGE_DESCRIPTION =
  'Allow Vouch to schedule serious deadline alarms for tasks that are almost due.';
const APP_INTENTS_PACKAGE_FILE = 'VouchAlarmKitAppIntentsPackage.swift';

function withAlarmKitInfoPlist(config) {
  return withInfoPlist(config, (nextConfig) => {
    nextConfig.modResults.NSAlarmKitUsageDescription =
      nextConfig.modResults.NSAlarmKitUsageDescription || ALARM_KIT_USAGE_DESCRIPTION;
    return nextConfig;
  });
}

function withAlarmKitAppIntentsPackage(config) {
  return withXcodeProject(config, (nextConfig) => {
    const project = nextConfig.modResults;
    const projectRoot = nextConfig.modRequest.projectRoot;
    const projectName = nextConfig.modRequest.projectName;

    if (!projectName) {
      throw new Error('[withAlarmKit] Unable to resolve iOS project name.');
    }

    const sourceRoot = IOSConfig.Paths.getSourceRoot(projectRoot);
    const appIntentsPackagePath = path.join(sourceRoot, APP_INTENTS_PACKAGE_FILE);
    fs.writeFileSync(appIntentsPackagePath, buildAppIntentsPackageSource());

    const projectFilePath = `${projectName}/${APP_INTENTS_PACKAGE_FILE}`;
    if (!project.hasFile(projectFilePath)) {
      IOSConfig.XcodeUtils.addBuildSourceFileToGroup({
        filepath: projectFilePath,
        groupName: projectName,
        project,
      });
    }

    return nextConfig;
  });
}

function buildAppIntentsPackageSource() {
  return `// @generated by plugins/withAlarmKit.js
#if canImport(AppIntents) && canImport(VouchAlarmKit)
import AppIntents
import VouchAlarmKit

@available(iOS 26.0, *)
struct VouchAppIntentsPackage: AppIntentsPackage {
  static var includedPackages: [any AppIntentsPackage.Type] {
    [VouchAlarmKitAppIntentsPackage.self]
  }
}
#endif
`;
}

module.exports = function withAlarmKit(config) {
  config = withAlarmKitInfoPlist(config);
  config = withAlarmKitAppIntentsPackage(config);
  return config;
};
